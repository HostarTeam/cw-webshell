export function parsePort(port: string): number {
	const portAsNumber = parseInt(port);
	if (!isNaN(portAsNumber) && portAsNumber > 0 && portAsNumber < 65536)
		return portAsNumber;

	throw new InvalidArgumentError(
		'Invalid port, port must be a valid integer and between 1 and 65535'
	);
}

export function validateHost(host: string): string {
	if (isValidHostname(host)) return host;

	throw new InvalidArgumentError(
		'Invalid host, host must be a valid hostname'
	);
}
